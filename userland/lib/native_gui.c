static inline void put_px(struct ng_fb *fb, int x, int y, uint32_t c) {
    if (!fb || !fb->ptr) return;
    if (x < 0 || y < 0 || (uint32_t)x >= fb->width || (uint32_t)y >= fb->height) return;
    uint8_t *p = fb->ptr + (size_t)y * fb->pitch + (size_t)x * (fb->bpp / 8U);
    if (fb->bpp == 32U) {
        *(uint32_t *)p = c;
    } else if (fb->bpp == 24U) {
        p[0] = (uint8_t)(c & 0xFFU);
        p[1] = (uint8_t)((c >> 8) & 0xFFU);
        p[2] = (uint8_t)((c >> 16) & 0xFFU);
    }
}

int ng_fb_open(struct ng_fb *fb) {
    struct ng_fb_var_screeninfo v;
    struct ng_fb_fix_screeninfo f;
    if (!fb) return -1;
    memset(fb, 0, sizeof(*fb));
    fb->fd = open("/dev/fb0", O_RDWR, 0);
    if (fb->fd < 0) return -1;
    if (ng_syscall3(SYS_IOCTL, fb->fd, NG_FBIOGET_VSCREENINFO, (long)&v) < 0 ||
        ng_syscall3(SYS_IOCTL, fb->fd, NG_FBIOGET_FSCREENINFO, (long)&f) < 0) {
        close(fb->fd);
        fb->fd = -1;
        return -1;
    }
    fb->width = v.xres;
    fb->height = v.yres;
    fb->bpp = v.bits_per_pixel;
    fb->pitch = f.line_length;
    fb->map_len = (size_t)fb->pitch * fb->height;
    fb->ptr = (uint8_t *)mmap(0, fb->map_len, PROT_READ | PROT_WRITE, MAP_SHARED, fb->fd, 0);
    if ((long)fb->ptr < 0) {
        close(fb->fd);
        fb->fd = -1;
        fb->ptr = 0;
        return -1;
    }
    return 0;
}

void ng_fb_close(struct ng_fb *fb) {
    if (!fb) return;
    if (fb->ptr && (long)fb->ptr > 0) munmap(fb->ptr, fb->map_len);
    if (fb->fd >= 0) close(fb->fd);
    memset(fb, 0, sizeof(*fb));
    fb->fd = -1;
}

void ng_fill(struct ng_fb *fb, uint32_t rgb) {
    ng_rect(fb, 0, 0, (int)fb->width, (int)fb->height, rgb);
}

void ng_rect(struct ng_fb *fb, int x, int y, int w, int h, uint32_t rgb) {
    int i, j;
    if (!fb || !fb->ptr || w <= 0 || h <= 0) return;
    for (j = 0; j < h; j++) {
        for (i = 0; i < w; i++) {
            put_px(fb, x + i, y + j, rgb);
        }
    }
}

void ng_frame_3d(struct ng_fb *fb, int x, int y, int w, int h, uint32_t light, uint32_t dark) {
    ng_rect(fb, x, y, w, 1, light);
    ng_rect(fb, x, y, 1, h, light);
    ng_rect(fb, x, y + h - 1, w, 1, dark);
    ng_rect(fb, x + w - 1, y, 1, h, dark);
}

void ng_char(struct ng_fb *fb, int x, int y, char c, uint32_t fg, uint32_t bg) {
    int cx, cy;
    uint8_t uc = (uint8_t)c;
    const uint8_t *g;
    if (uc < 32U || uc > 127U) uc = '?';
    g = g_font8x8[uc - 32U];
    for (cy = 0; cy < 8; cy++) {
        uint8_t bits = g[cy];
        for (cx = 0; cx < 8; cx++) {
            put_px(fb, x + cx, y + cy, (bits & (1U << cx)) ? fg : bg);
        }
    }
}

void ng_text(struct ng_fb *fb, int x, int y, const char *s, uint32_t fg, uint32_t bg) {
    size_t i, n;
    if (!s) return;
    n = strlen(s);
    for (i = 0; i < n; i++) {
        ng_char(fb, x + (int)i * 8, y, s[i], fg, bg);
    }
}

void ng_cursor(struct ng_fb *fb, int x, int y, uint32_t fg) {
    int i;
    for (i = 0; i < 12; i++) {
        put_px(fb, x, y + i, fg);
        if (i < 8) put_px(fb, x + i, y + i, fg);
    }
}

char ng_keycode_to_ascii(uint16_t code, int shift) {
    static const char plain[] = {
        ['1'] = '1'
    };
    (void)plain;
    switch (code) {
        case NG_KEY_SPACE: return ' ';
        case NG_KEY_MINUS: return shift ? '_' : '-';
        case NG_KEY_EQUAL: return shift ? '+' : '=';
        case NG_KEY_COMMA: return shift ? '<' : ',';
        case NG_KEY_DOT: return shift ? '>' : '.';
        case NG_KEY_SLASH: return shift ? '?' : '/';
        case NG_KEY_SEMICOLON: return shift ? ':' : ';';
        case NG_KEY_APOSTROPHE: return shift ? '"' : '\'';
        case NG_KEY_LEFTBRACE: return shift ? '{' : '[';
        case NG_KEY_RIGHTBRACE: return shift ? '}' : ']';
        case NG_KEY_BACKSLASH: return shift ? '|' : '\\';
        case NG_KEY_GRAVE: return shift ? '~' : '`';
        case NG_KEY_1: return shift ? '!' : '1';
        case NG_KEY_2: return shift ? '@' : '2';
        case NG_KEY_3: return shift ? '#' : '3';
        case NG_KEY_4: return shift ? '$' : '4';
        case NG_KEY_5: return shift ? '%' : '5';
        case NG_KEY_6: return shift ? '^' : '6';
        case NG_KEY_7: return shift ? '&' : '7';
        case NG_KEY_8: return shift ? '*' : '8';
        case NG_KEY_9: return shift ? '(' : '9';
        case NG_KEY_0: return shift ? ')' : '0';
        default:
            if (code >= NG_KEY_A && code <= NG_KEY_Z) {
                char base = (char)('a' + (code - NG_KEY_A));
                return shift ? (char)(base - 32) : base;
            }
            return 0;
    }
}

